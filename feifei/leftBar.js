$(".list-group-item").click(function () {
	$(".autoCollapseLeftBar").collapse('hide');
	if($(this).find('.glyphicon-menu-up').css('display')=="block")
	{
		$(".glyphicon-menu-up").css('display',"block");
		$(".glyphicon-menu-down").css('display',"none");
		$(this).find('.glyphicon-menu-down').css('display',"block");
		$(this).find('.glyphicon-menu-up').css('display',"none");
	}
	else
	{
		$(".glyphicon-menu-up").css('display',"block");
		$(".glyphicon-menu-down").css('display',"none");
		$(this).find('.glyphicon-menu-down').css('display',"none");
		$(this).find('.glyphicon-menu-up').css('display',"block");
	}
})